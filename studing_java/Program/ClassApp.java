

public class ClassApp {

	public static void main(String[] args) {
		
		System.out.println(Math.PI); //class란? 연관된 메소드와 변수를 묶어놓은 일종의 프로그램, 여기서 Math라는 클래스 안에 PI라는 메소드를 불러옴
		System.out.println(Math.floor(1.6)); //Math 라는 클래스 안에 . floor(내림)라는 메소드를 불러와 (1.6)을 거치는 결과값을 출력
		System.out.println(Math.ceil(1.6)); //Math 라는 클래스 안에 . ceil(올림)라는 메소드를 불러와 (1.6)을 거치는 결과값을 출력
		
		

	}

}
