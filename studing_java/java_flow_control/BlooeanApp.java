package java_flow_control;

public class BlooeanApp {

	public static void main(String[] args) {
		
		System.out.println("One"); // 문자열 데이터 타입String
		System.out.println(1); // 정수형 데이터 타입Integer
		
		System.out.println(true); //boolean 데이터 타입
		System.out.println(false); //boolean 데이터 타입
		
		String foo = "Hello world!";
		//String true = "Hello World!!";  "true" reserved word
		
		System.out.println(foo.contains("world"));
		System.out.println(foo.contains("nopecho"));
		
	}

}
