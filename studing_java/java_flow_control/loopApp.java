package java_flow_control;

public class loopApp {

	public static void main(String[] args) {
		System.out.println(1);
		System.out.println("====while====");
		int i =0;
		while(i<3) { 
			System.out.println(23);
			System.out.println(23);
			i++;
			
		}
		System.out.println("====for====");
		for (int j=0;j<3;j++) {
			System.out.println(23);
			System.out.println(23);
		}

	}

}
