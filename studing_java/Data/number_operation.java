
public class number_operation {

	public static void main(String[] args) {
		System.out.println(100+100); // 200
		System.out.println(100-99); // 1
		System.out.println(100*100); // 10000
		System.out.println(100/50); //2
		System.out.println(100%3);
		System.out.println(0.2*0.23);
		
		System.out.println(Math.PI);
		System.out.println(Math.floor(Math.PI));
		System.out.println(Math.abs(Math.PI));
		System.out.println(Math.ceil(Math.PI));

	}

}
