public class data_type{
	public static void main(String[] args) {
		System.out.println(6); // 숫자 6
		System.out.println("6"); // 문자열(String) 6
		System.out.println("씩쓰!");
		System.out.println(8+9); // = 17
		System.out.println("8"+"9"); // = 89(문자열을 결합시킴)
		System.out.println("sdashdjkashd".length()); // length() 문자열의 길이를 알려줌
		
		System.out.println('H'); // '' 작은따옴표는 문자 한 음절을 나타냄
		
		System.out.println('1'+'Y'+'U'+'N'); //=HYUN
		
	}
}